
public class Uyg1 {// for döngüsü ile sayıların toplamınu bulma

    public static void main(String[] args) {

        int toplam = 0;
        for (int i = 0; i <= 10; i++) {
            toplam += i;
        }
        System.out.println("1 den 10 a kadar  olan sayıların toplamı : " + toplam);
        toplam = 0;
        for (int i = 20; i <= 37; i++) {
            toplam += i;
        }
        System.out.println("20den 37ye kadar olan sayilarin toplami: " + toplam);

        toplam = 0;
        for (int i = 35; i <= 49; i++) {
            toplam += i;
        }
        System.out.println("35den 49a kadar olan sayilarin toplami: " +  + toplam);
            
    }

}
